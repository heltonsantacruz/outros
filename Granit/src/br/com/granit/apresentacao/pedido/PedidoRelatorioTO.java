package br.com.granit.apresentacao.pedido;

public class PedidoRelatorioTO {
	
	
	private String codigo;
	private Double desconto;
	private Double total;
	private Double subTotal;
	private String data;
	private String produtoString;
	private String finalizado;
	private String nomeFornecedor;
	
	
	
	public String getNomeFornecedor() {
		return nomeFornecedor;
	}




	public void setNomeFornecedor(String nomeFornecedor) {
		this.nomeFornecedor = nomeFornecedor;
	}




	public String getCodigo() {
		return codigo;
	}
	



	public Double getDesconto() {
		return desconto;
	}




	public void setDesconto(Double desconto) {
		this.desconto = desconto;
	}




	public Double getTotal() {
		return total;
	}




	public void setTotal(Double total) {
		this.total = total;
	}




	public Double getSubTotal() {
		return subTotal;
	}




	public void setSubTotal(Double subTotal) {
		this.subTotal = subTotal;
	}




	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	

	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getProdutoString() {
		return produtoString;
	}
	public void setProdutoString(String produtoString) {
		this.produtoString = produtoString;
	}
	public String getFinalizado() {
		return finalizado;
	}
	public void setFinalizado(String finalizado) {
		this.finalizado = finalizado;
	}
	
	
	

}
