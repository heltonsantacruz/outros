
UPDATE cliente 
    SET bairro=upper(bairro), email=upper(email), 
        endereco=upper(endereco), nome=upper(nome)
GO

UPDATE produto 
    SET descricao=upper(descricao)
GO

UPDATE fornecedor 
    SET bairro=upper(bairro), contatoFornecedor=upper(contatoFornecedor),
    email=upper(email), endereco=upper(endereco), nome=upper(nome) 
GO


UPDATE itemvenda 
    SET descricao=upper(descricao)
GO


UPDATE venda 
    SET observacao=upper(observacao) 
GO